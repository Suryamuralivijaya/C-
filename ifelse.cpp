//Sample program for decision making statements using if else statement
#include<iostream>
using namespace std;
int main()
{
	float a;
	cout<<"Enter the height of the person";
	cin>>a;
	if(a>=6.6)
	{
		cout<<"the person is eligible to play volley ball"<<a<<endl;
	}
	else
	{
		cout<<"the person is not eligible to play volley ball"<<endl;
	}
	return 0;
}
